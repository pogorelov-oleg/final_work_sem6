public class Notebook {
    String model;
    String color;
    String os;
    int hdd;
    int ram;

    public Notebook(String model, String color, String os, int hdd, int ram) {
        this.model = model;
        this.color = color;
        this.os = os;
        this.hdd = hdd;
        this.ram = ram;
    }
}